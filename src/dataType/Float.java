package dataType;

public class Float {

    public static void main(String[] args) {

        float marks = (float) 60.89;

        float my_maths_marks = 89.45f;

        double my_new_marks = 4345.986;

        System.out.println("My marks were: " + marks);
        System.out.println("My maths marks were: " + my_maths_marks);
        System.out.println("My new maths marks are: " + my_new_marks);

        //Downcasting
        int a = (int) 345.67;

        System.out.println("Type casted int value is: " + a);
    }
}
